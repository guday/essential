/**
 * 数组去重
 * @param arr   输入数组
 * @returns {Array} 输出数组
 */
var unique = function (arr) {
    var newArr = [];
    var map = {};
    for (var i in arr) {
        if (!map[arr[i]]) {
            map[arr[i]] = true;
            newArr.push(arr[i]);
        }
    }
    return newArr;
};




